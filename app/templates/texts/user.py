def load(cpu_percent: float, ram_usage: tuple) -> str:
    cpu_bars = int(cpu_percent / 5)
    ram_bars = int(ram_usage[2] / 5)     
    return USAGE % (
        '|' * cpu_bars,
        ' ' * (20 - cpu_bars),
        cpu_percent,
        '|' * ram_bars,
        ' ' * (20 - ram_bars),
        ram_usage[2],
    )


